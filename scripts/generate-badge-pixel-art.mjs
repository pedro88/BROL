// ---------------------------------------------------------------------------
// Cadres de rareté (canvas 20×20, icône en (2,2))
// ---------------------------------------------------------------------------

/** Retourne [x, y, charCouleur] à dessiner pour le cadre. */
function framePixels(rarity) {
  const px = [];
  const corners = [
    [0, 0, 1, 1],
    [19, 0, -1, 1],
    [0, 19, 1, -1],
    [19, 19, -1, -1],
  ];

  if (rarity === "uncommon") {
    for (const [cx, cy] of corners) px.push([cx, cy, "a"]);
  }
  if (rarity === "rare") {
    for (const [cx, cy, dx, dy] of corners) {
      px.push([cx, cy, "A"], [cx + dx, cy, "A"], [cx, cy + dy, "A"]);
    }
  }
  if (rarity === "epic") {
    for (const [cx, cy, dx, dy] of corners) {
      px.push(
        [cx, cy, "A"],
        [cx + dx, cy, "A"],
        [cx + 2 * dx, cy, "a"],
        [cx, cy + dy, "A"],
        [cx, cy + 2 * dy, "a"],
        [cx + dx, cy + dy, "L"]
      );
    }
  }
  if (rarity === "legendary") {
    for (const [cx, cy, dx, dy] of corners) {
      px.push(
        [cx, cy, "A"],
        [cx + dx, cy, "A"],
        [cx + 2 * dx, cy, "a"],
        [cx, cy + dy, "A"],
        [cx, cy + 2 * dy, "a"],
        [cx + dx, cy + dy, "L"]
      );
    }
    // Sparkles aux milieux des bords
    px.push([9, 0, "L"], [10, 0, "L"], [0, 9, "L"], [0, 10, "L"]);
    px.push([19, 9, "L"], [19, 10, "L"], [9, 19, "L"], [10, 19, "L"]);
  }
  return px;
}

// ---------------------------------------------------------------------------
// Rendu SVG
// ---------------------------------------------------------------------------

function parseGrid(raw) {
  const rows = raw.split("\n").filter((r) => r.length > 0);
  if (rows.length !== 16) throw new Error(`grid: ${rows.length} rows (expected 16)`);
  for (const [i, r] of rows.entries()) {
    if (r.length !== 16) throw new Error(`grid row ${i}: ${r.length} cols (expected 16)`);
  }
  return rows;
}

function renderSvg(design, rarity) {
  const { grid, extra = {} } = DESIGNS[design];
  const rows = parseGrid(grid);
  const palette = { ...BASE, ...RARITIES[rarity], ...extra };

  // Canvas 20×20 : cadre + icône décalée de (2,2)
  const cells = [];
  for (const [x, y, c] of framePixels(rarity)) {
    cells.push([x, y, palette[c]]);
  }
  for (let y = 0; y < 16; y++) {
    for (let x = 0; x < 16; x++) {
      const c = rows[y][x];
      if (c === ".") continue;
      const color = palette[c];
      if (color === undefined) throw new Error(`${design}: unknown char "${c}"`);
      if (color === null) continue; // transparent explicite
      cells.push([x + 2, y + 2, color]);
    }
  }

  // Fusion horizontale des runs de même couleur (réduit la taille du SVG)
  const byRow = new Map();
  for (const [x, y, color] of cells) {
    if (!byRow.has(y)) byRow.set(y, []);
    byRow.get(y).push([x, color]);
  }
  const rects = [];
  for (const [y, pixels] of [...byRow.entries()].sort((a, b) => a[0] - b[0])) {
    pixels.sort((a, b) => a[0] - b[0]);
    let run = null;
    for (const [x, color] of pixels) {
      if (run && run.color === color && x === run.x + run.w) {
        run.w++;
      } else {
        if (run) rects.push(run);
        run = { x, y, w: 1, color };
      }
    }
    if (run) rects.push(run);
  }

  const body = rects
    .map((r) => `<rect x="${r.x}" y="${r.y}" width="${r.w}" height="1" fill="${r.color}"/>`)
    .join("\n  ");

  return `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 20 20" shape-rendering="crispEdges">
  ${body}
</svg>
`;
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------

let count = 0;
for (const design of Object.keys(DESIGNS)) {
  const category = design.split("-")[0];
  const dir = path.join(OUT, category);
  fs.mkdirSync(dir, { recursive: true });
  for (const rarity of Object.keys(RARITIES)) {
    const svg = renderSvg(design, rarity);
    fs.writeFileSync(path.join(dir, `${design}-${rarity}.svg`), svg);
    count++;
  }
}
console.log(`${count} SVG générés dans ${OUT}`);
